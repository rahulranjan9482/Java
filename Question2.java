// Q:- Write a programming to print triangle using star pattern programming logic .
public class Question2 {
    public static void main(String[] args) {
        int n =7;
        for(int i=0 ;i<n;i++){
            for(int j=1; j<i;j++){
            System.out.print("*");
            }
            System.out.println();
        }
    }
}
