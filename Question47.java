// Q:- Write a java program to print the area and perimeter of a rectangle .
public class Question47 {
    public static void main(String[] args) {
        double w = 5.5;
        double h = 8.5;
        double a = w*h;
        double p = 2*(w+h);
        System.out.println("Area is : "+a);
        System.out.println("perimeter is : "+p);
    }
}
