// Q:- Write a java program to print the results of the following operations.

public class Question38 {
    public static void main(String[] args) {
        double a = -5+8*6;
        double b = (55+9)/9;
        double c = 20+3*5/8;
        double d = 5+15/3*2-8/3;
        System.out.println("-5+8*6 = "+a);
        System.out.println("(55+9)/9 = "+b);
        System.out.println("20+3*5/8 = "+c);
        System.out.println("5+15/3*2-8/3 = "+d);
    }
}
